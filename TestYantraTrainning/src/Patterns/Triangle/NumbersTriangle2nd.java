package Patterns.Triangle;

public class NumbersTriangle2nd {

	public static void main(String[] args) {
		
		for (int r = 1; r <= 5; r++) {
			{
				for (int c = 1; c <=5; c++) {
					if(c<=r)
						
					System.out.print(c+" ");
				}
				System.out.println();
					
				}
			}
		}

}
