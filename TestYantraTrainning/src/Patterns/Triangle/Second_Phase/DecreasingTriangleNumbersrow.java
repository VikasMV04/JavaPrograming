package Patterns.Triangle.Second_Phase;

public class DecreasingTriangleNumbersrow {

	public static void main(String[] args) {
		for (int r = 1; r <=5 ; r++)//rows 
		{
			for (int c = r; c <=5; c++) //columns
			{
				System.out.print(r);		
			}
			System.out.println();
		}
		System.out.println("********************");
		

	}

}
